package programs;

public class StudentEn {
	
	private int age;
	private double height;
	private String name;
	
	public void setData(int a, double b, String c) {
		age = a;
		height = b;
		name = c;
	}
	
	public int getAge() {
		return age;
	}
	
	public double getHeight() {
		return height;
	}
	
	public String getName () {
		return name;
	}

}
